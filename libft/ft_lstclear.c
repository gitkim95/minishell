#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*tmp;

	while (*lst)
	{
		tmp = *lst;
		(*del)(tmp -> content);
		*lst = tmp -> next;
		free(tmp);
	}
	*lst = NULL;
}
