#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	tmp;

	if (n < 0)
	{
		if (n == -2147483648)
		{
			write(fd, "-2147483648", 11);
			return ;
		}
		n *= -1;
		write(fd, "-", 1);
	}
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	tmp = n % 10 + '0';
	write(fd, &tmp, 1);
}
